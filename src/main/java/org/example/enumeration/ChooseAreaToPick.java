package org.example.enumeration;

/**
 * Enum indicating the area from which the player draws a card.
 * The player can draw a card from one of the following areas:
 * >DECKSAREA - The player draws from the deck area.
 * >VISIBLEAREA - The player draws from the visible cards area.
 */
public enum ChooseAreaToPick {
    DECKSAREA, VISIBLEAREA
}
